    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP
     * <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DatabaseManager dbCon = new DatabaseManager();
        ArrayList<Projectbugtrack> buglist = null;
        String status = "";
        String obj = request.getParameter("projId");
        System.out.println("Servlet Created................................");
        if (obj != null || obj != "") {
            try {
                buglist = dbCon.selectbugs(Integer.parseInt(obj));
                status = "success";
            } catch (Exception e) {
                e.printStackTrace();
                status = "fail";
            }
        }
        request.setAttribute("status", status);
        request.setAttribute("buglist", buglist);
        //  System.out.println(buglist);

        RequestDispatcher rd = request.getRequestDispatcher("showBugs.jsp?projId=" + obj);

        rd.forward(request, response);
    }

